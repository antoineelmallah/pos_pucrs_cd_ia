########################################
# Distribuição binomial:
########################################
dbinom(3,20,0.1) # dbinom(x,n,p)
barplot(dbinom(0:10,4,0.7))

########################################
# Distribuição binomial negativa
########################################
dnbinom(0:10,4,0.7)
barplot(dnbinom(0:10,4,0.7))

########################################
# Distribuição Poisson:
########################################
dpois(50,40) # dpois(x, lambda)

########################################
# Distribuição Hipergeométrica:
########################################
dhyper(4,100,200,4) # dhyper(x,m,n,k)

# onde:

#       m + n
#-------------------
#         k
#-------------------
#    m    |    n
#    x    |  k - x

